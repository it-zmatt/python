# A stack is a linear data structure that stores items in LIFO order (Last In, First Out)

# Implementation as a List
stack = []

# Pushing elements to the stack
stack.append("a")
stack.append("b")
stack.append("c")
stack.append("d")

# Printing the stack
print(stack)

# Popping elemnets from the stack
print(stack.pop())
print(stack.pop())

# Printing the stack
print(stack)